/**
 * Models a WordForm element
 *
 * Created by barry on 07/04/2017.
 */
class WordForm {
    private String writtenform;
    private String msd;

    String getWrittenform() {
        return writtenform;
    }

    void setWrittenform(String writtenform) {
        this.writtenform = writtenform;
    }

    String getMsd() {
        return msd;
    }

    void setMsd(String msd) {
        this.msd = msd;
    }
}
